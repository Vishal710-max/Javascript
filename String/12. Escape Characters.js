
// Escape Characters
// Special characters in strings:

// Escape Sequence	   Description
// \'	                Single quote
// \"	                Double quote
// \\	                Backslash
// \n	                New line
// \t	                Tab
// \r	                Carriage return


// Example:
let escapedText = "This is a \"quote\" and a new\nline.";
console.log(escapedText);
// Output:
// This is a "quote" and a new
// line.