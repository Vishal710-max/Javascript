

// Case Conversion (toUpperCase() & toLowerCase())
// We can convert a string to uppercase or lowercase using the toUpperCase() and toLowerCase() methods
//  These methods return a new string with all alphabetic characters converted to uppercase or lowercase, while leaving non-alphabetic characters unchanged.


// Syntax:
// let upperCaseString = string.toUpperCase();
// let lowerCaseString = string.toLowerCase();


let str = "Shreyash Patil";

console.log(`Upper Case ${str.toUpperCase()}`);    // Upper Case SHREYASH PATIL
console.log(`Lower case ${str.toLowerCase()}`);    // Lower case shreyash patil