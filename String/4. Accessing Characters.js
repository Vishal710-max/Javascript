
// Accessing Characters (charAt() & Bracket Notation)
// Access the perticular element of string using [] Barcket Notation or using function charAt()

// Syntax:
//   string.charAt(index)  // Returns character at given index
//   string[index]         // Alternative (modern way)

// Example:
let str = "Hello";
console.log(str.charAt(0))   // H
console.log(str[1])          // e


