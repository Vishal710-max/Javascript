
// Ternary operator:
// Ternary operator is a concise way to write conditional statements, it is widely used in JavaScript. It is also known as conditional operator
// It has three parts: a condition, statement to execute if the condition is true and the statement to execute if the condition is false.

// Syntax:
//   (condition) ? (statement1) : (statement2);


// Example:
let myage = 21;

(myage >= 18) ? (console.log("Can Drive")) : (console.log("Cannot Drive"));

// Output : Can Drive