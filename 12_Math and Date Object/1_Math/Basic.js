
// Math Object
// The Math object provides mathematical constants and functions (all static—no constructor needed).

// Key Properties (Constants)
// Property	    Description      	Value
// Math.PI	         Pi (π)	              3.141592653589793
// Math.E	         Euler's number	      2.718281828459045
// Math.LN2	     Natural log of 2	  0.6931471805599453
// Math.SQRT2	     Square root of 2	  1.4142135623730951


// Example:
console.log(Math.PI); // 3.141592653589793

console.log(Math.round(5.5)); // 6
console.log(Math.round(7.2)); // 7

console.log(Math.floor(8.9)); // 8
console.log(Math.floor(6.2)); // 6

console.log(Math.random()); // 0.123... (random between 0-1)
console.log(Math.max(10, 20, 5)); // 20
console.log(Math.min(10, 0, 200, 300, 25))    // 0
console.log(Math.pow(2, 5)); // 32 (2⁵)

console.log(Math.abs(-5))    // 5 

