
// Arrays:
// An array in JavaScript is a single variable that stores multiple elements (values) in an ordered list.
// Arrays can hold any data type (numbers, strings, objects, even other arrays).
// Arrays are zero-indexed (first element at index 0).
// Arrays are dynamic (can grow/shrink in size).


// Creating Arrays

// A) Using Array Literal (Most Common)
const fruits = ["Apple", "Banana", "Orange"];


// B) Using new Array() Constructor
const numbers = new Array(1, 2, 3);

// C) Creating an Empty Array
const emptyArray = [];
