

// for...of Loop
// Used to loop through iterable objects (arrays, strings, maps, sets, etc.).


// Syntax:
// for (let element of iterable) {
//     code to execute
// }


// Example:

arr = [10, 20, 30,  40];
for (let i of arr) {
    console.log(i);
}

// Output:
// 10
// 20
// 30
// 40