
// while Loop
// - Executes a block of code as long as a condition is true.
// - This is an entry controlled loop i.e Execute block if condition is true 


// Syntax:
//     while (condition) {
          // code to execute
//     }


// Example:
let i = 1;
while (i <= 5) {
    console.log("Count: "+i);
    i++;
}


// Output:
// Count: 1
// Count: 2
// Count: 3
// Count: 4
// Count: 5