

// for Loop
// Executes a block of code a specific number of times (with initialization, condition, and increment/decrement).
// A for loop is constituted with four parts: the initialization, the condition, increment/decrement and the code which has to be executed if the conditions are met true.


// Syntax:
//   for (initialization; condition; increment/decrement) {
         // code to execute
//   }


// Example:
for (let i = 1; i <=5; i++) 
    console.log("Count: " + i);


// Output:
// Count: 1
// Count: 2
// Count: 3
// Count: 4
// Count: 5