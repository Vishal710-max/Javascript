
// if..else Statement:
// - 'if-else' statement is a conditional statement that allows us to execute one block of code if a specified condition evaluates to true, 
//     and another block of code if the condition evaluates to false.

// Syntax:
//   if (condition) {
//     Code block to be executed if condition is true
//   } else {
//     Code block to be executed if condition is false
//   }


// Example:
let myAge = 15;

if(myAge >= 18){
    console.log("I can Drive");
}else{
    console.log("Cannot Drive");
}

// Output : "Cannot Drive"